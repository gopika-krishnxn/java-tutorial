public class arithmeticoperaters {
    public static void main(String[] args) {
        int x;
        x = 10 + 3;
        System.out.println(x);
        int y;
        y = 10 - 8;
        System.out.println(y);
        int z;
        z = 10 * 8;
        System.out.println(z);
        int k;
        k = 10 / 5;
        System.out.println(k);
        int l;
        l = 10 % 3;
        System.out.println(l);
        int m = 5;
        ++m;
        System.out.println(m);
        --m;
        System.out.println(m);

    }
}
