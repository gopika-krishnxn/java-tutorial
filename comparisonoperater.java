public class comparisonoperater {
    public static void main(String[] args) {
        int a=7,b=9;
        System.out.println(a==b);  //false
        System.out.println(a!=b);  //true   
        System.out.println(a>b);   //false
        System.out.println(a<b);   //true
        System.out.println(a>=b);  //false
        System.out.println(a<=b);  //true

    }
}
