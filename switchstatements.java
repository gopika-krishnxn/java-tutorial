public class switchstatements {
     public static void main(String[] args) {
        int myNumber=4;
        switch (myNumber) {
            case 1:
                System.out.println("ONE");
                break;
             case 2:
                System.out.println("TWO");
                break;
             case 3:
                System.out.println("THREE");
                break;
             case 4:
                System.out.println("FOUR");
                break;
        
            default:
                System.out.println("invalid number");
                break;
        }
     }
}
